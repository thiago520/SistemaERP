    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed
            mod.setFantasia_fornecedor((String) jComboBoxFornecedores.getSelectedItem());            
            mod.setPreco_custo(Double.parseDouble(jTextFieldCusto.getText().replace(',', '.')));
            mod.setId_produto(Integer.parseInt(lblTest.getText()));
            String data = new SimpleDateFormat("yyyy-MM-dd").format(jDateChooserData.getDate());        
            mod.setData(data); 
        if(valorBotao == 1){            
            control.InserirProForn(mod);             
        }
        if(valorBotao == 2){            
            control.AlterarProForn(mod);                
        }         
        ExecutaAtt();        
        dispose();
    }//GEN-LAST:event_jButtonGravarActionPerformed
    
    public void ReceberDados(ModeloProdutos btnF) {
        valorBotao = btnF.getBtnAlterar();
        lblTest.setText(String.valueOf(btnF.getId_produto()));
            
        if (valorBotao == 1) {
            jComboBoxFornecedores.setSelectedItem(0);
            jTextFieldCusto.setText("");
            jDateChooserData.setDate(null);
        }
        
        if (valorBotao == 2) {
            jComboBoxFornecedores.setSelectedItem(btnF.getFantasia_fornecedor()); 
            String preco_custo = String.valueOf(btnF.getPreco_custo());
            jTextFieldCusto.setText(preco_custo.replace('.', ','));           
            
            String dataEmUmFormato = btnF.getData();
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            
            try {
                Date data = formato.parse(dataEmUmFormato);
                formato.applyPattern("dd/MM/yyyy");
                String dataFormatada = formato.format(data);
                Date dataFinal = formato.parse(dataFormatada);
                jDateChooserData.setDate(dataFinal);            
            } catch (ParseException ex) {
                Logger.getLogger(FrmAddFornecedor.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }
    
