    private void jTableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutosMouseClicked
       String cod = ""+jTableProdutos.getValueAt(jTableProdutos.getSelectedRow(),0);
       connLProdutos.executaSQL("select * from produtos where id_produto='"+cod+"'");             
              
       try {                  
                    
            connLProdutos.rs.first();            
            mod.setId_produto(connLProdutos.rs.getInt("id_produto"));
            mod.setNome_produto(connLProdutos.rs.getString("nome_produto"));
            mod.setGtin_produto(connLProdutos.rs.getString("gtin_produto"));
            mod.setGtin_embalagem(connLProdutos.rs.getString("gtin_embalagem"));
            mod.setEstoquema(connLProdutos.rs.getInt("estoquema"));
            mod.setEstoquemi(connLProdutos.rs.getInt("estoquemi"));
            mod.setUnidade_produto(connLProdutos.rs.getString("unidade_produto"));            
            mod.setQtda_caixa(connLProdutos.rs.getDouble("qtda_caixa"));            
            mod.setPreco_venda(connLProdutos.rs.getDouble("preco_venda"));
            mod.setCategoria(connLProdutos.rs.getString("categoria"));
            mod.setEstoque(connLProdutos.rs.getDouble("estoque"));
                       
        } catch (SQLException s){
            JOptionPane.showMessageDialog(rootPane,"Erro ao selecionar os dados!\nErro: " + s);
        }
    }//GEN-LAST:event_jTableProdutosMouseClicked

