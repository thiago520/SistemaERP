    public void preencherTabela(String SQL){
        ArrayList dados = new ArrayList();
    
        String [] Colunas = new String[]{"Código","Nome","Unidade","Preço Venda"};
    
        connLProdutos.executaSQL(SQL);
        
        try {            
            if (!connLProdutos.rs.next()) {                
            } else {
                    connLProdutos.rs.first();
                    do{                       
                        dados.add(new Object[]{connLProdutos.rs.getInt("id_produto"),connLProdutos.rs.getString("nome_produto"),connLProdutos.rs.getString("unidade_produto"),formatoMoeda.format(connLProdutos.rs.getDouble("preco_venda"))});
                    }while(connLProdutos.rs.next());
                }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Erro ao preencher o ArrayList!\n Erro: "+ex);
        } 
        
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTableProdutos.setModel(modelo);
        jTableProdutos.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableProdutos.getColumnModel().getColumn(0).setResizable(true);
        jTableProdutos.getColumnModel().getColumn(1).setPreferredWidth(230);
        jTableProdutos.getColumnModel().getColumn(1).setResizable(true);
        jTableProdutos.getColumnModel().getColumn(2).setPreferredWidth(64);
        jTableProdutos.getColumnModel().getColumn(2).setResizable(true);
        jTableProdutos.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTableProdutos.getColumnModel().getColumn(3).setResizable(true);
        
        jTableProdutos.getTableHeader().setReorderingAllowed(false);
        jTableProdutos.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);        
        
    }  
    
