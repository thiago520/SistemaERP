    public void ResultadoPedido() {   
        connFinalizacao.conexao();
        connFinalizacao.executaSQL("select pedido.cod_cliente AS id,pedido.cod_pedido,clientes.nome_cliente,pedido.valor_total,pedido.status,pedido.desconto,(SELECT SUM(pedido.valor_pago) - (SUM(pedido.valor_total) - SUM(pedido.desconto)) FROM pedido WHERE pedido.cod_cliente = id AND pedido.status = 'Finalizado') AS SALDO  from pedido,clientes where pedido.cod_cliente = clientes.id_cliente and pedido.cod_pedido = " + jTextFieldCodigo.getText() + "");
        try {
            if (!connFinalizacao.rs.next()) {
                LimpaTela();
                JOptionPane.showMessageDialog(this, "Pedido não encontrado!");                
            } else {
                connFinalizacao.rs.first();
                jLabelCodigo.setText(String.valueOf(connFinalizacao.rs.getInt("cod_pedido")));                
                jLabelNome.setText(connFinalizacao.rs.getString("nome_cliente")); 
                jLabelValor.setText(String.valueOf(formatoMoeda.format(connFinalizacao.rs.getDouble("valor_total"))));
                jLabelStatus.setText(connFinalizacao.rs.getString("status")); 
                mod.setCod_pedido(connFinalizacao.rs.getInt("cod_pedido")); 
                mod.setValor_pago(connFinalizacao.rs.getBigDecimal("valor_total").subtract(connFinalizacao.rs.getBigDecimal("desconto")));
                saldo = connFinalizacao.rs.getDouble("SALDO");
            }
        } catch (SQLException ex) {            
        }
        connFinalizacao.desconecta();
    }
    
    public void FinalizaPedido() {                
        mod.setStatus("Finalizado");
        control.AlterarStatus(mod);
        control.FinalizaValorPago(mod);    
        jTextFieldCodigo.setText("");       
    }
    
