    private void jTextFieldCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldCodigoKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!"".equals(jTextFieldCodigo.getText())) {
                jLabelStatusFinalizado.setText("");
                ResultadoPedido();
            }
            if (saldo == 0) {
                if ("Em Preparo".equals(jLabelStatus.getText()) || "Enviado".equals(jLabelStatus.getText())) {  
                    jLabelStatusFinalizado.setText("Pedido "+ mod.getCod_pedido() +" Finalizado");
                    FinalizaPedido();
                } else {
                    JOptionPane.showMessageDialog(this, "Não é possível finalizar este pedido! Status: " + jLabelStatus.getText());
                }            
            } else {                
                JOptionPane.showMessageDialog(this, "Este pedido possui saldo em haver/devedor. Será necessário finalizar manualmente!");
                LimpaTela();
            }
        }
    }//GEN-LAST:event_jTextFieldCodigoKeyPressed

